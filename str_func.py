def str_upp(string):
    """
    Функция форматироватирования строки
    :param string: строка
    :return: строка в верхнем регистре
    """
    return string.upper()


def str_title(string):
    """
    Функция изменяет первые буквы слов в строке на верхний регистр
    :param string: строка
    :return: первые буквы слов в строке в верхем регистре
    """
    return string.title()